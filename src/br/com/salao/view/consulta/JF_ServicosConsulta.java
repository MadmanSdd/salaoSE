    private void buscar_Servico() {
        Servico_Controller c = new Servico_Controller();

        modelo = (DefaultTableModel) jTable1.getModel();
        modelo.getDataVector().clear();//Limpando tabela para n√£o duplica valores

        for (Object obj : c.lista_Servico()) {
            Map row = (Map) obj;

            modelo.addRow(new Object[]{
                row.get("id"),
                row.get("nome"),
                row.get("preco"),
                row.get("obs")

            });

        }

    }

