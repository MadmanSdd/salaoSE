        // TODO add your handling code here:
        Icon icone = new ImageIcon("delete-database.png");

        if (txt_Nome.getText().equals("") || jff_cel.getText().equals("(  )  -    -    ")) {
            JOptionPane.showMessageDialog(null, "Campos obrigatorios *", "InformaÃ§Ã£o...", 1);
        } else {
            try {

                Clientes_Controller c = new Clientes_Controller();;
                c.gravar(new Clientes(txt_Nome.getText(), jff_tel.getText(), jff_cel.getText(), txt_Rua.getText(), txt_Numero.getText(), txt_Bairro.getText()));

                //Limpando campos
                txt_Bairro.setText("");
                txt_Nome.setText("");
                txt_Numero.setText("");
                txt_Rua.setText("");
                jff_tel.setText("");
                jff_cel.setText("");
                jjbox_Sexo.setSelectedIndex(0);

                JOptionPane.showMessageDialog(null, "Novo cliente adicionado", "Salvo...", 1);
            } catch (JDBCConnectionException e) {
                JOptionPane.showMessageDialog(null, "NÃ£o foi possivel encontrar o banco de dados\n"
                        + e.getMessage(), "Erro", JOptionPane.WARNING_MESSAGE);
            }
        }


